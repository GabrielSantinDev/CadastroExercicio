    public void configCampos() {
        ConfiguraCampos cc = new ConfiguraCampos();
        
        cc.configTextField(txtEmail, "Email");
        cc.configTextField(txtNomeCompleto, "Nome Completo");
        cc.configPasswordField(psfSenha, "Senha");
        cc.configPasswordField(psfConfirmarSenha, "Confirmar Senha");
    }
    
    public Usuario retornaObjeto() {
        Usuario user = new Usuario();
        
        user.setEmail(txtEmail.getText());
        user.setNomeCompleto(txtNomeCompleto.getText());
        user.setSenha(String.valueOf(psfSenha.getPassword()));
        
        if (rdbAluno.isSelected()) user.setFuncao("Aluno");
        if (rdbProfessor.isSelected()) user.setFuncao("Professor");
        if (rdbDiretor.isSelected()) user.setFuncao("Diretor");
        if (rdbSecretario.isSelected()) user.setFuncao("Secretario");
       
        System.out.println("Usu√°rio adicionado: " + user.toString());
        
        return user;
    }
    
    public boolean verificaCampos() {
        boolean valido = true;
        
        ValidaCampos v = new ValidaCampos();
        
        if (v.validaTextField(txtEmail, "Email")) valido = false;
        if (v.validaTextField(txtNomeCompleto, "Nome Completo")) valido = false;
        if (v.validaPasswordField(psfSenha, "Senha")) valido = false;
        if (v.validaPasswordField(psfConfirmarSenha, "Confirmar Senha")) valido = false;
        
        if (v.validaFuncao(rdbAluno, rdbDiretor, rdbProfessor, rdbSecretario)) valido = false;
        
        if (v.confirmarSenha(psfSenha, psfConfirmarSenha)) {
            valido = false;
            JOptionPane.showMessageDialog(null, "As senhas n√£o coincidem.");
        }
        
        return valido;
    }
